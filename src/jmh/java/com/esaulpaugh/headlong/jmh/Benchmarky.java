package com.esaulpaugh.headlong.jmh;

import com.esaulpaugh.headlong.abi.Function;
import com.esaulpaugh.headlong.abi.Tuple;
import com.esaulpaugh.headlong.util.Strings;
import org.openjdk.jmh.annotations.Benchmark;
import org.openjdk.jmh.annotations.BenchmarkMode;
import org.openjdk.jmh.annotations.Fork;
import org.openjdk.jmh.annotations.Level;
import org.openjdk.jmh.annotations.Mode;
import org.openjdk.jmh.annotations.Scope;
import org.openjdk.jmh.annotations.Setup;
import org.openjdk.jmh.annotations.State;
import org.openjdk.jmh.annotations.Warmup;

import java.math.BigInteger;
import java.nio.ByteBuffer;

@State(Scope.Benchmark)
public class Benchmarky {

    private Function f;
    private Tuple args;

    @Setup(Level.Trial)
    public void setUp() {
        f = new Function("sam(bytes,bool,uint256[])");
        args = Tuple.of(
                Strings.decode("dave", Strings.UTF_8),
                true,
                new BigInteger[] { BigInteger.valueOf(1), BigInteger.valueOf(2), BigInteger.valueOf(3) }
        );
    }

    @Benchmark
    @Fork(value = 1, warmups = 1)
    @BenchmarkMode(Mode.Throughput)
    @Warmup(iterations = 1)
    public ByteBuffer headlong_encode_function_call() {
        return f.encodeCall(args);
    }
}
